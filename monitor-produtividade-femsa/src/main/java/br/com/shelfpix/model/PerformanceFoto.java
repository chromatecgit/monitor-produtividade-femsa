package br.com.shelfpix.model;

public class PerformanceFoto {
	private double tiradas;
	private double recebidas;
	private double evidencia;
	private double ocorrencia;
	private double processaveis;
	private double automatico;
	private double manual;
	private double descarte;
	private double publicadas;

	public double getTiradas() {
		return tiradas;
	}

	public void setTiradas(double tiradas) {
		this.tiradas = tiradas;
	}

	public double getRecebidas() {
		return recebidas;
	}

	public void setRecebidas(double recebidas) {
		this.recebidas = recebidas;
	}

	public double getEvidencia() {
		return evidencia;
	}

	public void setEvidencia(double evidencia) {
		this.evidencia = evidencia;
	}

	public double getOcorrencia() {
		return ocorrencia;
	}

	public void setOcorrencia(double ocorrencia) {
		this.ocorrencia = ocorrencia;
	}

	public double getProcessaveis() {
		return processaveis;
	}

	public void setProcessaveis(double processaveis) {
		this.processaveis = processaveis;
	}

	public double getAutomatico() {
		return automatico;
	}

	public void setAutomatico(double automatico) {
		this.automatico = automatico;
	}

	public double getManual() {
		return manual;
	}

	public void setManual(double manual) {
		this.manual = manual;
	}

	public double getDescarte() {
		return descarte;
	}

	public void setDescarte(double descarte) {
		this.descarte = descarte;
	}

	public double getPublicadas() {
		return publicadas;
	}

	public void setPublicadas(double publicadas) {
		this.publicadas = publicadas;
	}

}
